package com.example.hdgg.estello;

public class Kontakt {

    String username;
    String upasswort;


    /**
     * Get Methode Username
     * @return username
     */
    public String getUsername() {
        return username;
    }

    /**
     * Set Methode
     * @param username
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * Get Methode Userpasswort
     * @return upasswort
     */
    public String getUpasswort() {
        return upasswort;
    }

    /**
     * Set Methode
     * @param upasswort
     */
    public void setUpasswort(String upasswort) {
        this.upasswort = upasswort;
    }
}
